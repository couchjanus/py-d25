# Повторный проброс исключений в Python
# Однажды выведенные исключения продолжают обрабатываться 
# в вызывающих их методах до тех пор, пока они не будут обработаны. 
# Но можно добавить условие except, которое содержит только вызов [raise] 
# без каких-либо аргументов. Это приведет к повторному исключению.

try:
    # Намеренное выведение исключения.
    raise Exception('I learn Python!')
except:
    # Повторное выведение исключения.
    print("Entered in except.")    
    raise 